def add_title(text, level=0, bold=True, size=None, color=None, align=WD_ALIGN_PARAGRAPH.CENTER):
    p = doc.add_paragraph()
    p.alignment = align
    run = p.add_run(text)
    run.bold = bold
    if size:
        run.font.size = Pt(size)
    if color:
        run.font.color.rgb = RGBColor(*color)
    return p

def add_toc_line(text, indent=0, bold=False, size=11, spacing=6):
    p = doc.add_paragraph()
    pf = p.paragraph_format
    pf.space_before = Pt(spacing)
    pf.space_after = Pt(0)
    pf.left_indent = Cm(indent)
    run = p.add_run(text)
    run.bold = bold
    run.font.size = Pt(size)
    run.font.name = '宋体'
    return p

