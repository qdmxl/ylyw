# ============================================================
# 审查引擎
# ============================================================
class PhysicsChecker:
    """第二层：物理合规性审查"""
    
    def check(self, text):
        issues = []
        
        for pattern, description, severity, domain in PHYSICS_CONSTRAINTS:
            match = re.search(pattern, text)
            if match:
                matched_text = match.group(0)
                issues.append({
                    "type": "物理违规",
                    "severity": severity,
                    "message": description,
                    "evidence": f"匹配文本: \"{matched_text[:50]}\" | 领域: {domain}",
                    "domain": domain
                })
        
        return issues


