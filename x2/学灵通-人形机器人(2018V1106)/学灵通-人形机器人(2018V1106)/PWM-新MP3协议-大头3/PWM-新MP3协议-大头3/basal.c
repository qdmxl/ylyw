//函数名称：void sit_down(uchar step)
//函数说明：蹲下子程序，使机器人处于微蹲状态，便于行走。
//入口参数：setp, 表示下蹲的程度。
//出口参数：无
//#############################################################################
void sit_down(uchar step)
{        
         uchar i;
	 
         for(i=0;i<step;i++)
		    {
			   position[0]--;              //左腿下蹲
			   position[1]+=2;             
			   position[2]++;               
			   
               position[3]++;              //右腿下蹲
			   position[4]-=2;              
			   position[5]--;               
			   PWM_24();
               low_level_500u(25);
			}			
}
