// 函数名称：low_level_500u(uchar time)
// 函数说明：PWM信号低电平时间子程序，控制舵机PWM信号的低电平时间决定舵机转动的速度
// 入口参数：低电平时间，500u为基础
// 出口参数：无
//#############################################################################
void low_level_500u(sint16 time)
{
  sint16 i; 
  for(i=0;i<time;i++)
  {delay500us(1);}
}
