// 函数名称：void array( )
// 函数说明：排序子程序，将各个口的8位根据时间的长短排序
// 入口参数：无
// 出口参数：无
//#############################################################################
void array()
{ 
	uchar i=0,j=0,x=0;
	     pick_up[0]=0xFE;
         pick_up[1]=0xFD;
         pick_up[2]=0xFB;
         pick_up[3]=0xF7;
         pick_up[4]=0xEF;
         pick_up[5]=0xDF;
         pick_up[6]=0xBF;
         pick_up[7]=0x7F;
	//排序
	for(i=0;i<=6;i++)
	{         for(j=i+1;j<=7;j++)
         {
                if(arr[i]<arr[j])
                  {             
				            
                           x=arr[j];
						   arr[j]=arr[i];
						   arr[i]=x;
					  
					       x=pick_up[j];
						   pick_up[j]=pick_up[i];
                           pick_up[i]=x;  
                  }
         }}	
	for(i=0;i<=6;i++)
         {
                   arr[i]= arr[i]- arr[i+1];
         } 		
}
//#############################################################################
// 函数名称：int max(uchar total，int M)
// 函数说明：求数组中绝对值最大的元素的子程序
// 入口参数：无
// 出口参数：无
//#############################################################################
int max(uchar total,int M)
{ uchar i;
	//找最大
	for(i=0;i<total;i++)
	{   if (abs(position_change[i])>M)
	        M=abs(position_change[i]);
	}	
		return M;			 
}
//#############################################################################
//函数名称：void p_to_p(int delay)
//函数说明：position 变化子程序，实现同起同落
//入口参数：delay=延时，define:p-to-p决定循环次数由自己决定还是通过程序算出结果决定
//出口参数：无
//#############################################################################
void p_to_p(int delay,int define)
{
   uchar i,j;
   int a,b=0,M=0;
   float c;
   uchar position_buffer[24];	        

   for(i=0;i<24;i++)
   {
   	  position_buffer[i]=position[i];
   }   
   if(define==0)					 //决定循环次数M
   {  M=1;
      M=max(24,M);
	 if(M>50)
       M=50;
	 else if (M>20)
	   M=20;
   }   
   else
   {M=define; 	 					 //循环次数决定完毕
	 }
   for(i=1;i<=M;i++)				 //开始循环M次输出
   {
      for(j=0;j<24;j++)
      {  c=position_change[j];	     //将距离目标点的值分数，一步一步逼近目标值 
         if(i==1)		  
	        a=(c*i)/M;		
		 else
		 {
		    a=(c*i)/M;
			b=(c*(i-1))/M;
		 }
         position[j]+=(a-b);
		}
		PWM_24();
    low_level_500u(delay);
   }  				 		  		 
   for(i=0;i<24;i++) 		  		 //最后补偿每个舵机的误差  		 
   {
      position[i]=position_buffer[i]+position_change[i];
      position_change[i]=0;
   }
  PWM_24();
  low_level_500u(10);   
}

