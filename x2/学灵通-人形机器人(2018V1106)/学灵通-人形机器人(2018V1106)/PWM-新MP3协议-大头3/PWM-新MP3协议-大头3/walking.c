//函数名称： void left_cs(uchar setp)
//函数说明：左侧身子程序
//入口参数：step, 表示左侧身的幅度
//出口参数：无
//#############################################################################
void left_cs(uchar setp)
{ uchar i;
   for (i=0;i<setp;i++)
   {
		position[8]-=1;
		position[9]+=1;
		position[10]-=1;
		position[11]+=1;
		PWM_24();
        low_level_500u(18);
   }
}
