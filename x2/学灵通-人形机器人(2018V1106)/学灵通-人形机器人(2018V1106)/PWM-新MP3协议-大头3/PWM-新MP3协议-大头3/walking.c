//#############################################################################
//函数名称： void walk(uchar setp)
//函数说明：前进子程序
//入口参数：step, 表示前进的步数
//出口参数：无
//#############################################################################
void walk(uchar setp)					
{	
		uchar  i;
		//mp3_send(0x00,0x00,30);		
		sit_down(50);
		delay500ms(1);
		
		//抬右腿半步子程序
		left_cs(12);						//左侧身	
		right_up(10);						//抬右腿
		right_forward(12);                 //迈右腿               
		right_down(10);                    //放右腿                     
		right_cs(12);                      //右侧身                         
			 
			 //前进循环
		  for(i=0;i<setp/2;i++)
		  {	  	//mp3_send(0x00,0x00,0x02);
 			 right_cs(12);                   //右侧身   
			 left_up(10);						//抬左腿
 			 left_forward(24);					//迈左腿
             left_down(10);						//放左腿
			 left_cs(12);						//左侧身
				
			  
			 left_cs(12);						//左侧身
			 right_up(10);						//抬右腿
			 right_forward(24);					//迈右腿
			 right_down(10);                    //放右腿  
			 right_cs(12);                      //右侧身


			 right_cs(12);                      //右侧身   
			 left_up(10);						//抬左腿
 			 left_forward(24);					//迈左腿
             left_down(10);						//放左腿
			 left_cs(12);						//左侧身
				
			  
			 left_cs(12);						//左侧身
			 right_up(10);						//抬右腿
			 right_forward(24);					//迈右腿
			 right_down(10);                    //放右腿  
			 right_cs(12);                      //右侧身
				
		  }	
		  	//抬左腿半步子程序 
			 right_cs(12);                      //右侧身
			 left_up(10);						//抬左腿
 			 left_forward(12);					//迈左腿
             left_down(10);						//放左腿
			 left_cs(12);						//左侧身

			 stand_up(50);			 	   
}

