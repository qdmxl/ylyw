# 实用函数
# ============================================================
def get_fuzzy_membership(radical):
    """返回一个部首的8维模糊隶属度向量"""
    if radical in radical_bagua_fuzzy:
        return radical_bagua_fuzzy[radical]["membership"]
    # 未知部首返回均匀小值
    return [0.1] * 8

def get_dominant_bagua(radical):
    """返回部首归属最强的八卦"""
    if radical not in radical_bagua_fuzzy:
        return "未知"
    mem = radical_bagua_fuzzy[radical]["membership"]
    bagua_names = ["乾","兑","离","震","巽","坎","艮","坤"]
    return bagua_names[mem.index(max(mem))]

def get_semantic_affinity(char, radicals_list):
    """计算一个汉字（由多个部首组成）对8类语义的综合隶属度"""
    combined = [0.0] * 8
    weight_sum = 0
    for rad in radicals_list:
        if rad in radical_bagua_fuzzy:
            mem = radical_bagua_fuzzy[rad]["membership"]
            for j in range(8):
                combined[j] += mem[j]
            weight_sum += 1
    if weight_sum == 0:
        return [0.1] * 8
    return [c / weight_sum for c in combined]


